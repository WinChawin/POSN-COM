#include <iostream>
using namespace std;

// bool - ค่าความจริง (true/false)

int main(){
    bool isTrue = true; // lowercase true - 1
    bool isFalse = false; // lowercase false - 0
    cout << isTrue << " " << isFalse << endl;
    cout << not isTrue << " " << not isFalse << endl; // ! (not)
    return 0;
}

